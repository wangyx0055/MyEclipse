package advertise;

public class AdvertiseObj {
	
	private int id = -1;
	private String serviceName = "";
	private String mtAdvertise = "";
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getMtAdvertise() {
		return mtAdvertise;
	}
	public void setMtAdvertise(String mtAdvertise) {
		this.mtAdvertise = mtAdvertise;
	}
		
}
