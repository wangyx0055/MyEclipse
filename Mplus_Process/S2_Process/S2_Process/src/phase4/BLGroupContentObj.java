package phase4;

public class BLGroupContentObj {
	
	private int id = 0;
	private String userId = "";
	private String groupName = "";
	private String info = "";
	private String timeSendMo = "";
	private int active = 0;
	private int statusMT = 0;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getTimeSendMo() {
		return timeSendMo;
	}
	public void setTimeSendMo(String timeSendMo) {
		this.timeSendMo = timeSendMo;
	}
	public int getActive() {
		return active;
	}
	public void setActive(int active) {
		this.active = active;
	}
	public int getStatusMT() {
		return statusMT;
	}
	public void setStatusMT(int statusMT) {
		this.statusMT = statusMT;
	}
	
}
